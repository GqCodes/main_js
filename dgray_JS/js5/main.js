// Math Methods & Properties
console.log(Math.PI);
console.log(Math.trunc(Math.PI));
console.log(Math.round(3.5));
// rounds up
console.log(Math.ceil(Math.PI));
// rounds down
console.log(Math.floor(Math.PI));
// .pow(a, b) exponets or to the power of
console.log(Math.pow(2, 10));
// .min() returns smallest number passed in
console.log(Math.min(7, 1, 86, 3, 9.472));
// .max() returns largest number passed in
console.log(Math.max(7, 1, 86, 3, 9.472));
// .random() returns a random number from 0 to 1
console.log(Math.random());
// .random() from 1 - 10
console.log(Math.floor(Math.random() * 10) + 1);
